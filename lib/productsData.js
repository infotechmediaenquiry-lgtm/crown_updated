// Particular section ke saare products nikalne ke liye
export const getProductsBySection = (sectionSlug) => {
  return Object.values(productsData).filter(
    product => product.sectionSlug === sectionSlug
  );
};

// Single product nikalne ke liye by ID
export const getProductById = (id) => {
  return productsData[parseInt(id)] || null;
};

// Particular section ke related products nikalne ke liye (same section, different product)
export const getRelatedProducts = (currentProductId, sectionSlug, limit = 4) => {
  return Object.values(productsData)
    .filter(p => p.sectionSlug === sectionSlug && p.id !== currentProductId)
    .slice(0, limit);
};

// All products array (for search functionality)
export const getAllProducts = () => {
  return Object.values(productsData);
};

